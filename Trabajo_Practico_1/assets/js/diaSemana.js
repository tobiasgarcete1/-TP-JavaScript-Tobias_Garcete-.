let dia = parseInt(prompt("Ingrese un dia de la semana"));

switch (dia){
    case 1:
        console.log("lunes");
        break
    case 2:
        console.log("Martes");
        break
    case 3:
        console.log("Miercoles");
        break
    case 4:
        console.log("Jueves");
        break
    case 5:
        console.log("Viernes");
        break
    case 6: 
    console.log("Sabado");
        break
    case 7:
        console.log("Domingo");
        break
    default:
        console.log("Ingrese un numero valido")    
}